"use strict";

const fs = require("fs");
const path = require("path");

const root = path.resolve(__dirname, "..");
const sources = [
  "js/modules/00-state-config.js",
  "js/modules/01-storage-api-session.js",
  "js/modules/02-backup-customers.js",
  "js/modules/03-sales-workflow.js",
  "js/modules/04-navigation-settings.js",
  "js/modules/05-dashboard-reports.js",
  "js/modules/06-returns-inventory-purchases.js",
  "js/modules/07-sync-shortcuts-expiry-init.js"
];

const sourceText = sources.map(file => {
  const text = fs.readFileSync(path.join(root, file), "utf8");
  return `\n/* Source: ${file} */\n${text}\n`;
}).join("");
const handlerSearchText = `${fs.readFileSync(path.join(root, "index.html"), "utf8")}\n${sourceText}`;
const declarations = new Set();

for (const match of sourceText.matchAll(/^(?:async\s+)?function\s+([A-Za-z_$][\w$]*)\s*\(/gm)) {
  declarations.add(match[1]);
}
for (const match of sourceText.matchAll(/^(?:const|let|var)\s+([A-Za-z_$][\w$]*)\s*=/gm)) {
  declarations.add(match[1]);
}

const referencedHandlers = new Set();
for (const attribute of handlerSearchText.matchAll(/\bon(?:click|change|input|submit|keydown|keyup|blur|focus)\s*=\s*(["'])([\s\S]*?)\1/gi)) {
  for (const call of attribute[2].matchAll(/\b([A-Za-z_$][\w$]*)\s*\(/g)) {
    if (declarations.has(call[1])) referencedHandlers.add(call[1]);
  }
}

const handlers = [...referencedHandlers].sort();
if (!declarations.has("initializeApplication")) {
  throw new Error("initializeApplication must be declared by the application sources");
}

const output = [
  "/* Generated by scripts/build-esm-runtime.cjs. Edit js/modules/* instead. */",
  'import { renderHtml as moduleRenderHtml } from "./core/dom-renderer.js";',
  sourceText,
  `\nconst browserEventHandlers = Object.freeze({\n  ${handlers.join(",\n  ")}\n});`,
  "Object.assign(globalThis, browserEventHandlers);",
  "export { initializeApplication, browserEventHandlers };",
  ""
].join("\n");

fs.writeFileSync(path.join(root, "js", "app-runtime.js"), output, "utf8");
console.log(`Built js/app-runtime.js with ${handlers.length} HTML event-handler exports.`);
